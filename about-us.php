                              </div>
                              <p>
                                In January 2000, Impact Chemicals was registered as a business partnership by two youthful entrepreneurs who would eventually become its founding directors.  At the time the business did not have any operating premise but conducted manufacturing activities within the living room of the two partners. The company mainly specialized in the manufacture of beauty products like body lotions, curl activator gels, perfumes, hair shampoos and conditioners, and to a very limited extent a few detergents for domestic use. Impact’s main customers at the time were beauty shops and salons. These products were sold door to door from salon to salon by the two partners who at one time also operated a small distribution outlet.
                              </p>
                              <p>
                                In April 2004, the company was incorporated and became a limited liability company with the two partners as the founding directors. At the same time company moved out of the estates to their first ever industrial premise which was a single room on the 1st floor of Laki house along Lunga Lunga road. The focus of manufacturing shifted from the manufacture of cosmetics to pharmaceutical antiseptics and disinfectants.
                              </p>
                              <p>Impact then started expanding its product base from the two main products-methylated and surgical spirit and also started acquiring clients within the larger Nairobi and away from the residential estate. All the deliveries to customers then were done by public transport.  In 2005, the company acquired its first vehicle a Datsun 1200 pick-up which became very useful in making deliveries to customers. A salesman and driver were employed to boost operations.</p>
                              <p>
                                Later the company’s sales activities spread out to towns like Mombasa, Kisumu, Bungoma, Kitale, Machakos, etc.
                              </p>
                              <p>First forward, today Impact Chemicals Ltd stands tall among industry peers with over 30 highly valued products  and a stronger passion to grow.

